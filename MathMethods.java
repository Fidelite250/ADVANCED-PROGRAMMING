public class MathMethods {

    static int sum(int a, int b) {
        return a + b;
    }

    static int product(int a, int b) {
        return a * b;
    }

    public static void main(String[] args) {
        System.out.println("Sum = " + sum(4, 6));
        System.out.println("Product = " + product(4, 6));
    }
}
